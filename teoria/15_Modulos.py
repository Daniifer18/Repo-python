# Modulos

# Tienen que tener una estructura para poder usarlos
# El primer caracter no puede ser un numero

# Para importar una funcion en especifico se escribe asi:
# from module import (nombre de la funcion) suma

import module 

print(f"La suma de los numeros es {module.suma(5,5)}")
print(f"La resta de los numeros es {module.resta(5,2)}")
print(f"La miltiplicacion de los numeros es {module.multiplicacion(5,7)}")
print(f"La division de los numeros es {module.division(10,5)}")